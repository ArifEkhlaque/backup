//first way//
function add(a, b) {
    var x = a
    var y = b
    document.getElementById("add").innerText = ("add:-" + x + "+" + y + "=" + (x + y))
}
function subtract(c, d) {
    var x = c
    var y = d
    document.getElementById("subtract").innerText = ("subtract:-" + x + "-" + y + "=" + (x - y))
}
function multiply(e, f) {
    var x = e
    var y = f
    document.getElementById("multiply").innerText = ("multiply:-" + x + "x" + y + "=" + (x * y))
}
function divide(g, h) {
    var x = g
    var y = h
    document.getElementById("divide").innerText = ("divide:-" + x + "/" + y + "=" + (x / y))
}
